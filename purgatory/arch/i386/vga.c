#define VBE_DISPI_INDEX_ENABLE           0x4
#define VBE_DISPI_DISABLED               0x00

#define VBE_DISPI_IOPORT_INDEX           0x01CE
#define VBE_DISPI_IOPORT_DATA            0x01CF

static void stdvga_sequ_write(unsigned char index, unsigned char value)
{
	outw((value << 8)|index, 0x3c4);
}

static void stdvga_grdc_write(unsigned char index, unsigned char value)
{
    outw((value<<8) | index, 0x3ce);
}

static void get_font_access(void)
{
    stdvga_sequ_write(0x00, 0x01);
    stdvga_sequ_write(0x02, 0x04);
    stdvga_sequ_write(0x04, 0x07);
    stdvga_sequ_write(0x00, 0x03);
    stdvga_grdc_write(0x04, 0x02);
    stdvga_grdc_write(0x05, 0x00);
    stdvga_grdc_write(0x06, 0x04);
}

static void release_font_access(void)
{
    unsigned short v;
    stdvga_sequ_write(0x00, 0x01);
    stdvga_sequ_write(0x02, 0x03);
    stdvga_sequ_write(0x04, 0x03);
    stdvga_sequ_write(0x00, 0x03);
    v = (inb(0x3cc) & 0x01) ? 0x0e : 0x0a;
    stdvga_grdc_write(0x06, v);
    stdvga_grdc_write(0x04, 0x00);
    stdvga_grdc_write(0x05, 0x10);
}

static void stdvga_load_font(void *src, unsigned short count, unsigned short start, 
                 unsigned char destflags, unsigned char fontsize)
{
    unsigned short i;
    unsigned char *dest = (void *)0xa0000;

    get_font_access();


    for (i = 0; i < count; i++)
        memcpy(dest + i * 32, src + i * fontsize, fontsize);
                   
    release_font_access();
}

static void dispi_write(unsigned short reg, unsigned short val)
{
    outw(reg, VBE_DISPI_IOPORT_INDEX);
    outw(val, VBE_DISPI_IOPORT_DATA);
}

